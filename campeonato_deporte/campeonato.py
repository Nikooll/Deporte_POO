class Campeonato:
    def __init__(self, duracion_compromiso, fecha_fin, fecha_inicio, genero, numero_clasificados_por_grupo, numero_equipos_por_grupo, numero_grupos, numero_vueltas):
        self.duracion_compromiso = duracion_compromiso
        self.fecha_fin = fecha_fin
        self.fecha_inicio = fecha_inicio
        self.genero = genero
        self.numero_clasificados_por_grupo = numero_clasificados_por_grupo
        self.numero_equipos_por_grupo = numero_equipos_por_grupo
        self.numero_grupos = numero_grupos
        self.numero_vueltas = numero_vueltas
